export function WaveText({
  text,
  incoming = false,
  className,
}: {
  text: string;
  incoming?: boolean;
  className?: string;
}) {
  return (
    <span
      className={cn(incoming ? "wave-text-in" : "wave-text", className)}
      aria-label={text}
    >
      {text.split("").map((character, index) =>
        character === " " ? (
          " "
        ) : (
          <b
            key={`${character}-${index}`}
            aria-hidden
            style={
              {
                "--char-delay": `${100 + index * (incoming ? 120 : 20)}ms`,
              } as CSSProperties
            }
          >
            {character}
          </b>
        ),
      )}
    </span>
  );
}

